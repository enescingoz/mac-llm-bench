#!/usr/bin/env python3
"""Generate results README.md files from raw JSON benchmark data.

Structure:
  results/
    README.md                         — Overview index
    {generation}/
      README.md                       — Generation overview with links to variants
      {variant}/
        README.md                     — Combined benchmark overview (speed + quality)
        speed/
          README.md                   — Speed-only leaderboard
        quality/
          coding/
            README.md                 — HumanEval+ quality results
          reasoning/
            README.md                 — Placeholder (future)
          general/
            README.md                 — Placeholder (future)
        raw/{chip}_{cpu}c-{gpu}g_{ram}gb/*.json

Run: python3 scripts/generate_results.py
"""

import json
import os
import glob
from collections import defaultdict

RESULTS_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "results")

GENERATIONS = ["m1", "m2", "m3", "m4", "m5"]
VARIANTS = ["base", "pro", "max", "ultra"]

CONTRIB_LINK = "[CONTRIBUTING.md](../../CONTRIBUTING.md)"
GUIDE_LINK = "[GUIDE.md](../../GUIDE.md)"

AUTOGEN_COMMENT = "<!-- Auto-generated by generate_results.py. Do not edit manually. -->\n"


def parse_chip_folder(folder_name):
    """Parse 'm5_10c-10g_32gb' or 'm4-pro_14c-20g_48gb'."""
    parts = folder_name.split("_")
    if len(parts) < 3:
        return None

    chip = parts[0]
    if "-" in chip:
        generation, variant = chip.split("-", 1)
    else:
        generation = chip
        variant = "base"

    cores = parts[1]
    cpu_cores = cores.split("c-")[0] if "c-" in cores else "?"
    gpu_cores = cores.split("-")[1].replace("g", "") if "-" in cores else "?"

    ram = parts[2].replace("gb", "")

    return {
        "generation": generation,
        "variant": variant,
        "cpu_cores": int(cpu_cores) if cpu_cores != "?" else 0,
        "gpu_cores": int(gpu_cores) if gpu_cores != "?" else 0,
        "ram_gb": int(ram) if ram.isdigit() else 0,
        "folder": folder_name,
    }


def load_all_results():
    """Load all results: {generation: {variant: {config_folder: [results]}}}

    Structure: results/{gen}/{variant}/raw/{chip_folder}/{runtime}/*.json
    where runtime is 'gguf' or 'mlx'.
    """
    data = defaultdict(lambda: defaultdict(lambda: defaultdict(list)))

    for gen in GENERATIONS:
        for variant in VARIANTS:
            raw_dir = os.path.join(RESULTS_DIR, gen, variant, "raw")
            if not os.path.isdir(raw_dir):
                continue

            for chip_dir in sorted(glob.glob(os.path.join(raw_dir, "*"))):
                if not os.path.isdir(chip_dir):
                    continue
                folder_name = os.path.basename(chip_dir)

                # Scan runtime subfolders (gguf/, mlx/)
                for runtime_dir in sorted(glob.glob(os.path.join(chip_dir, "*"))):
                    if not os.path.isdir(runtime_dir):
                        continue
                    runtime = os.path.basename(runtime_dir)

                    for json_file in sorted(glob.glob(os.path.join(runtime_dir, "*.json"))):
                        try:
                            with open(json_file) as f:
                                result = json.load(f)
                            # Tag with runtime if not already present
                            if "runtime" in result and "name" in result["runtime"]:
                                result["_runtime_tag"] = runtime
                            else:
                                result["_runtime_tag"] = runtime
                            data[gen][variant][folder_name].append(result)
                        except (json.JSONDecodeError, IOError):
                            continue

    return data


def chip_display_name(folder_name):
    """'m5_10c-10g_32gb' -> 'M5 - 10 CPU / 10 GPU / 32GB'"""
    info = parse_chip_folder(folder_name)
    if not info:
        return folder_name
    gen = info["generation"].upper()
    variant = info["variant"]
    if variant != "base":
        gen = f"{gen} {variant.capitalize()}"
    return f"{gen} - {info['cpu_cores']} CPU / {info['gpu_cores']} GPU / {info['ram_gb']}GB"


def variant_display(gen, variant):
    """'m5', 'pro' -> 'M5 Pro'"""
    gen_upper = gen.upper()
    if variant == "base":
        return gen_upper
    return f"{gen_upper} {variant.capitalize()}"


def render_speed_table(results):
    """Render a speed-only markdown table. Columns: Model | Quant | Runtime | pp128 | pp256 | pp512 | tg128 | tg256 | Memory."""
    # Check if we have multiple runtimes
    runtimes = set(r.get("_runtime_tag", "gguf") for r in results)
    show_runtime = len(runtimes) > 1

    lines = []
    if show_runtime:
        lines.append("| Model | Quant | Runtime | pp128 | pp256 | pp512 | tg128 | tg256 | Memory |")
        lines.append("|-------|-------|---------|------:|------:|------:|------:|------:|-------:|")
    else:
        lines.append("| Model | Quant | pp128 | pp256 | pp512 | tg128 | tg256 | Memory |")
        lines.append("|-------|-------|------:|------:|------:|------:|------:|-------:|")

    # Sort by tg128 descending
    sorted_results = sorted(results, key=lambda r: r.get("speed", {}).get("tg128", 0), reverse=True)

    for r in sorted_results:
        m = r.get("model", {})
        s = r.get("speed", {})
        mem = r.get("memory", {})
        runtime = r.get("_runtime_tag", "gguf").upper()

        name = m.get("name", "?")
        quant = m.get("quant", "?")
        pp128 = f"{s['pp128']:.1f}" if "pp128" in s else "-"
        pp256 = f"{s['pp256']:.1f}" if "pp256" in s else "-"
        pp512 = f"{s['pp512']:.1f}" if "pp512" in s else "-"
        tg128 = f"{s['tg128']:.1f}" if "tg128" in s else "-"
        tg256 = f"{s['tg256']:.1f}" if "tg256" in s else "-"
        peak_mem = f"{mem['peak_rss_gb']:.2f}" if "peak_rss_gb" in mem else "-"

        if show_runtime:
            lines.append(
                f"| {name} | {quant} | {runtime} | {pp128} | {pp256} | {pp512} | {tg128} | {tg256} | {peak_mem} |"
            )
        else:
            lines.append(
                f"| {name} | {quant} | {pp128} | {pp256} | {pp512} | {tg128} | {tg256} | {peak_mem} |"
            )

    return "\n".join(lines)


def render_quality_coding_table(results):
    """Render a quality/coding markdown table. Only models with quality data.

    Columns: Model | Quant | Runtime | HumanEval+ | HumanEval (base) | Eval Framework
    Sorted by HumanEval+ descending.
    Returns (table_str, has_data) where has_data=False means no quality results exist.
    """
    quality_results = [
        r for r in results
        if r.get("quality", {}).get("humaneval_plus_pass1") is not None
    ]

    if not quality_results:
        return "", False

    runtimes = set(r.get("_runtime_tag", "gguf") for r in quality_results)
    show_runtime = len(runtimes) > 1

    lines = []
    if show_runtime:
        lines.append("| Model | Quant | Runtime | HumanEval+ | HumanEval (base) | Eval Framework |")
        lines.append("|-------|-------|---------|----------:|----------------:|----------------|")
    else:
        lines.append("| Model | Quant | HumanEval+ | HumanEval (base) | Eval Framework |")
        lines.append("|-------|-------|----------:|----------------:|----------------|")

    # Sort by HumanEval+ descending
    sorted_results = sorted(
        quality_results,
        key=lambda r: r.get("quality", {}).get("humaneval_plus_pass1") or 0,
        reverse=True,
    )

    for r in sorted_results:
        m = r.get("model", {})
        q = r.get("quality", {})
        runtime = r.get("_runtime_tag", "gguf").upper()

        name = m.get("name", "?")
        quant = m.get("quant", "?")

        he_plus_raw = q.get("humaneval_plus_pass1")
        he_plus = f"{he_plus_raw * 100:.1f}%" if he_plus_raw is not None else "-"

        he_base_raw = q.get("humaneval_pass1")
        he_base = f"{he_base_raw * 100:.1f}%" if he_base_raw is not None else "-"

        eval_fw = q.get("framework", "EvalPlus")

        if show_runtime:
            lines.append(
                f"| {name} | {quant} | {runtime} | {he_plus} | {he_base} | {eval_fw} |"
            )
        else:
            lines.append(
                f"| {name} | {quant} | {he_plus} | {he_base} | {eval_fw} |"
            )

    return "\n".join(lines), True


def render_combined_table(results):
    """Render a combined overview table.

    Columns: Model | Quant | Runtime | tg128 | HumanEval+ | Memory
    Sort by HumanEval+ when available, then by tg128.
    """
    runtimes = set(r.get("_runtime_tag", "gguf") for r in results)
    show_runtime = len(runtimes) > 1

    lines = []
    if show_runtime:
        lines.append("| Model | Quant | Runtime | tg128 | HumanEval+ | Memory |")
        lines.append("|-------|-------|---------|------:|-----------:|-------:|")
    else:
        lines.append("| Model | Quant | tg128 | HumanEval+ | Memory |")
        lines.append("|-------|-------|------:|-----------:|-------:|")

    has_quality = any(
        r.get("quality", {}).get("humaneval_plus_pass1") is not None
        for r in results
    )

    if has_quality:
        sorted_results = sorted(
            results,
            key=lambda r: (
                r.get("quality", {}).get("humaneval_plus_pass1") is not None,
                r.get("quality", {}).get("humaneval_plus_pass1") or 0,
            ),
            reverse=True,
        )
    else:
        sorted_results = sorted(results, key=lambda r: r.get("speed", {}).get("tg128", 0), reverse=True)

    for r in sorted_results:
        m = r.get("model", {})
        s = r.get("speed", {})
        mem = r.get("memory", {})
        runtime = r.get("_runtime_tag", "gguf").upper()

        name = m.get("name", "?")
        quant = m.get("quant", "?")
        tg128 = f"{s['tg128']:.1f}" if "tg128" in s else "-"
        peak_mem = f"{mem['peak_rss_gb']:.2f}" if "peak_rss_gb" in mem else "-"

        he_plus_raw = r.get("quality", {}).get("humaneval_plus_pass1")
        he_plus = f"{he_plus_raw * 100:.1f}%" if he_plus_raw is not None else "-"

        if show_runtime:
            lines.append(
                f"| {name} | {quant} | {runtime} | {tg128} | {he_plus} | {peak_mem} |"
            )
        else:
            lines.append(
                f"| {name} | {quant} | {tg128} | {he_plus} | {peak_mem} |"
            )

    return "\n".join(lines)


# ─── Speed README (e.g., results/m5/base/speed/README.md) ─────────
def generate_speed_readme(gen, variant, configs):
    """Generate speed/README.md for a single variant."""
    display = variant_display(gen, variant)
    lines = [AUTOGEN_COMMENT]
    lines.append(f"# Speed Benchmarks — {display}\n")
    lines.append("All speed values are in **tokens/second** (higher = better). Memory is peak RSS in GB.\n")
    lines.append("[← Back to overview](../README.md) | [Code Quality →](../quality/coding/README.md)\n")

    if not configs:
        lines.append("No benchmark results yet for this chip variant.\n")
        lines.append(f"**Your contribution is welcome!** See {GUIDE_LINK} and {CONTRIB_LINK}.\n")
        return "\n".join(lines)

    for folder_name in sorted(configs.keys()):
        results = configs[folder_name]
        if not results:
            continue
        config_display = chip_display_name(folder_name)
        lines.append(f"## {config_display}\n")
        lines.append(render_speed_table(results))
        lines.append("")

    lines.append("---\n")
    lines.append(
        "*Auto-generated from raw benchmark data in [`../raw/`](../raw/). "
        "Run `python3 scripts/generate_results.py` to regenerate.*"
    )

    return "\n".join(lines)


# ─── Quality/Coding README ──────────────────────────────────────────
def generate_quality_coding_readme(gen, variant, configs):
    """Generate quality/coding/README.md for a single variant."""
    display = variant_display(gen, variant)
    lines = [AUTOGEN_COMMENT]
    lines.append(f"# Code Quality Benchmarks — {display}\n")
    lines.append("**HumanEval+ (EvalPlus) — 164 problems, 80x expanded test suite**\n")
    lines.append("[← Back to overview](../../README.md) | [Speed Benchmarks →](../../speed/README.md)\n")

    if not configs:
        lines.append("No benchmark results yet for this chip variant.\n")
        return "\n".join(lines)

    any_quality_data = False
    for folder_name in sorted(configs.keys()):
        results = configs[folder_name]
        if not results:
            continue

        config_display = chip_display_name(folder_name)
        table_str, has_data = render_quality_coding_table(results)

        if has_data:
            any_quality_data = True
            lines.append(f"## {config_display}\n")
            lines.append(table_str)
            lines.append("")

    if not any_quality_data:
        lines.append(
            "No quality benchmarks available yet for this configuration.\n\n"
            "Run `./bench_quality.sh --model <model-id>` to generate quality results, "
            f"then regenerate tables with `python3 scripts/generate_results.py`. "
            f"See {GUIDE_LINK} for details.\n"
        )

    lines.append("---\n")
    lines.append(
        "*Auto-generated from raw benchmark data. "
        "Run `python3 scripts/generate_results.py` to regenerate.*"
    )

    return "\n".join(lines)


