# ─── Generation README (e.g., results/m5/README.md) ───────────────
def generate_generation_readme(gen, variants_data):
    """Generate README for a generation folder — overview with links to variants."""
    gen_upper = gen.upper()
    lines = [AUTOGEN_COMMENT]
    lines.append(f"# Apple {gen_upper} Benchmarks\n")
    lines.append(f"Select your {gen_upper} chip variant:\n")

    lines.append("| Variant | Configurations | Models Tested | Status |")
    lines.append("|---------|----------------|---------------|--------|")

    for variant in VARIANTS:
        display = variant_display(gen, variant)
        configs = variants_data.get(variant, {})

        total_results = sum(len(r) for r in configs.values())
        total_configs = sum(1 for r in configs.values() if r)
        model_names = set()
        for results in configs.values():
            for r in results:
                model_names.add(r.get("model", {}).get("name", "?"))

        if total_results > 0:
            status = f"{total_configs} config(s), {len(model_names)} model(s)"
            lines.append(f"| [{display}]({variant}/) | {total_configs} | {len(model_names)} | {status} |")
        else:
            lines.append(f"| [{display}]({variant}/) | - | - | Awaiting contributions |")

    lines.append(f"\nSee {CONTRIB_LINK} for how to add your results.\n")

    lines.append("---\n")
    lines.append("*Auto-generated — run `python3 scripts/generate_results.py` to regenerate.*")

    return "\n".join(lines)


# ─── Top-level index (results/README.md) ──────────────────────────
def generate_index_readme(all_data):
    """Generate the main results/README.md index."""
    lines = [AUTOGEN_COMMENT]
    lines.append("# Benchmark Results\n")
    lines.append("Community-contributed benchmark results for LLMs on Apple Silicon.\n")
    lines.append("Browse by chip generation, then select your variant (base, Pro, Max, Ultra).\n")

    lines.append("| Generation | Variants with Data | Total Configs | Total Models | Link |")
    lines.append("|------------|--------------------|---------------|--------------|------|")

    for gen in GENERATIONS:
        gen_upper = gen.upper()
        variants_data = all_data.get(gen, {})

        variants_with_data = 0
        total_configs = 0
        model_names = set()

        for variant in VARIANTS:
            configs = variants_data.get(variant, {})
            has_data = any(len(r) > 0 for r in configs.values())
            if has_data:
                variants_with_data += 1
                total_configs += sum(1 for r in configs.values() if r)
                for results in configs.values():
                    for r in results:
                        model_names.add(r.get("model", {}).get("name", "?"))

        if variants_with_data > 0:
            lines.append(
                f"| **{gen_upper}** | {variants_with_data} | {total_configs} | {len(model_names)} | "
                f"[View results]({gen}/) |"
            )
        else:
            lines.append(f"| **{gen_upper}** | - | - | - | [Awaiting contributions]({gen}/) |")

    # Sub-links section: for variants that have data, show speed and quality links
    lines.append("\n## Sub-Leaderboards\n")
    lines.append("Direct links to dedicated speed and quality leaderboards:\n")

    lines.append("| Chip | Speed | Code Quality |")
    lines.append("|------|-------|--------------|")

    for gen in GENERATIONS:
        variants_data = all_data.get(gen, {})
        for variant in VARIANTS:
            configs = variants_data.get(variant, {})
            total_results = sum(len(r) for r in configs.values())
            if total_results > 0:
                display = variant_display(gen, variant)
                speed_link = f"[Speed]({gen}/{variant}/speed/README.md)"
                quality_link = f"[Code Quality]({gen}/{variant}/quality/coding/README.md)"
                lines.append(f"| {display} | {speed_link} | {quality_link} |")

    if not any(
        sum(len(r) for r in variants_data.get(variant, {}).values()) > 0
        for gen in GENERATIONS
        for variants_data in [all_data.get(gen, {})]
        for variant in VARIANTS
    ):
        lines.append("| — | No data yet | — |")

    # Quick comparison table
    all_model_names = set()
    for gen_data in all_data.values():
        for variant_data in gen_data.values():
            for results in variant_data.values():
                for r in results:
                    all_model_names.add(r.get("model", {}).get("name", "?"))

    if all_model_names:
        lines.append("\n## Quick Comparison\n")
        lines.append("Best `tg128` (text generation, tok/s) per generation:\n")

        header = "| Model |"
        sep = "|-------|"
        for gen in GENERATIONS:
            header += f" {gen.upper()} |"
            sep += "------:|"
        header += " HumanEval+ |"
        sep += "-----------:|"
        lines.append(header)
        lines.append(sep)

        for model_name in sorted(all_model_names):
            row = f"| {model_name} |"
            best_he_plus = None
            for gen in GENERATIONS:
                best = 0
                for variant_data in all_data.get(gen, {}).values():
                    for results in variant_data.values():
                        for r in results:
                            if r.get("model", {}).get("name") == model_name:
                                tg128 = r.get("speed", {}).get("tg128", 0)
                                if tg128 > best:
                                    best = tg128
                                he_plus = r.get("quality", {}).get("humaneval_plus_pass1")
                                if he_plus is not None:
                                    if best_he_plus is None or he_plus > best_he_plus:
                                        best_he_plus = he_plus
                row += f" {best:.1f} |" if best > 0 else " - |"
            row += f" {best_he_plus * 100:.1f}% |" if best_he_plus is not None else " - |"
            lines.append(row)

        lines.append("\n*Best tg128 across all variants within each generation. HumanEval+ is pass@1 (higher = better).*\n")

    lines.append("---\n")
    lines.append("*Auto-generated — run `python3 scripts/generate_results.py` to regenerate.*")

    return "\n".join(lines)


# ─── Main ─────────────────────────────────────────────────────────
def main():
    print("Loading results...")
    all_data = load_all_results()

    total = sum(
        len(results)
        for gen_data in all_data.values()
        for variant_data in gen_data.values()
        for results in variant_data.values()
    )
    print(f"Found {total} benchmark result(s)")

    # Ensure all generation/variant sub-folders exist
    for gen in GENERATIONS:
        for variant in VARIANTS:
            os.makedirs(os.path.join(RESULTS_DIR, gen, variant, "raw"), exist_ok=True)
            os.makedirs(os.path.join(RESULTS_DIR, gen, variant, "speed"), exist_ok=True)
            os.makedirs(os.path.join(RESULTS_DIR, gen, variant, "quality", "coding"), exist_ok=True)
            os.makedirs(os.path.join(RESULTS_DIR, gen, variant, "quality", "reasoning"), exist_ok=True)
            os.makedirs(os.path.join(RESULTS_DIR, gen, variant, "quality", "general"), exist_ok=True)

    # Generate per-variant files
    for gen in GENERATIONS:
        for variant in VARIANTS:
            configs = all_data.get(gen, {}).get(variant, {})
            base_dir = os.path.join(RESULTS_DIR, gen, variant)

            # speed/README.md
            speed_path = os.path.join(base_dir, "speed", "README.md")
            content = generate_speed_readme(gen, variant, configs)
            with open(speed_path, "w") as f:
                f.write(content)

            # quality/coding/README.md
            quality_coding_path = os.path.join(base_dir, "quality", "coding", "README.md")
            content = generate_quality_coding_readme(gen, variant, configs)
            with open(quality_coding_path, "w") as f:
                f.write(content)

            # quality/reasoning/README.md (placeholder)
            quality_reasoning_path = os.path.join(base_dir, "quality", "reasoning", "README.md")
            content = generate_quality_reasoning_readme(gen, variant)
            with open(quality_reasoning_path, "w") as f:
                f.write(content)

            # quality/general/README.md (placeholder)
            quality_general_path = os.path.join(base_dir, "quality", "general", "README.md")
            content = generate_quality_general_readme(gen, variant)
            with open(quality_general_path, "w") as f:
                f.write(content)

            # README.md (combined)
            readme_path = os.path.join(base_dir, "README.md")
            content = generate_variant_readme(gen, variant, configs)
            with open(readme_path, "w") as f:
                f.write(content)

            n_results = sum(len(r) for r in configs.values())
            status = f"{n_results} results" if configs else "awaiting"
            print(f"  {gen}/{variant}/ ({status}): README.md + speed/ + quality/coding/ + quality/reasoning/ + quality/general/")

    # Generate generation READMEs (overview with links to variants)
    for gen in GENERATIONS:
        variants_data = all_data.get(gen, {})
        readme_path = os.path.join(RESULTS_DIR, gen, "README.md")
        content = generate_generation_readme(gen, variants_data)
        with open(readme_path, "w") as f:
            f.write(content)
        print(f"  {gen}/README.md")

    # Generate top-level index
    index_path = os.path.join(RESULTS_DIR, "README.md")
    content = generate_index_readme(all_data)
    with open(index_path, "w") as f:
        f.write(content)
    print(f"  README.md")

    print("Done!")


